/* 
  算術演算子
*/
// 加算（addition）
let add = 10 + 5;
console.log(add); // 15

// 文字列の連結
let str = "Java" + "Script";
console.log(str); // JavaScript

let result1 = 3 + 3;
let result2 = "3" + "3";

console.log(result1); // 6
console.log(result2); // 33

// 減算（subtract）
let sub = 10 - 5;
console.log(sub); // 5

// 乗算（multiplication）
let mul = 10 * 5;
console.log(mul); // 50

// 除算（division）
let div = 10 / 5;
console.log(div); // 2

// インクリメント（increment）
let incr = 10;
incr++; // 現在の値に1を加える
console.log(incr);  // 11

// デクリメント（decrement）
let decr = 10;
decr--; // 現在の値から1を引く
console.log(decr);  // 9

/* 
  代入演算子
*/
// 加算して代入
let add2 = 1;
add2 += 2;
console.log(add2);  // 3

// 文字列を連結して代入
let str2 = "Java";
str2 += "Script";
console.log(str2);  // JavaScript

// 減算して代入
let sub2 = 10;
sub2 -= 5;
console.log(sub2);  // 5

// 乗算して代入
let mul2 = 10;
mul2 *= 5;
console.log(mul2);  // 50

// 除算して代入
let div2 = 10;
div2 /= 5;
console.log(div2);  // 2